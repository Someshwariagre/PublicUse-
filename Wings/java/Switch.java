class Switch{

public static void main (String[] args){

int month = 10;

switch (month){
 
case 1 : System.out.println(" The 1st month is JAN");
break;

case 2 : System.out.println(" The 2nd month is FEB");
break;

case  3 :System.out.println(" The 3rd month is MAR");
break;

case  4:System.out.println(" The 4th month is APRIL");
break;

case  5 :System.out.println(" The 5th month is MAY");
break;

case  6 :System.out.println(" The 6th month is JUN");
break;

case  7 :System.out.println(" The 7th month is JUL");
break;
case  8 :System.out.println(" The 8th month is AUG");
break;

case  9 :System.out.println(" The 9th month is SEPT");
break;

case 10 :System.out.println(" The 10th month is OCT");
break;

case  11 :System.out.println(" The 11th month is NOV");
break;

case  12 :System.out.println(" The 12th month is DEC");
break;

default:System.out.println("The value range must be between 1-12");
}
}
}

