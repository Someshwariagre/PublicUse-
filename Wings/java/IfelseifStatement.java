class IfelseifStatement{
public static void main(String[] args) {
  
String name = "smita";  
if(name == "sampati") {  
System.out.println("name is sampati");  
}
else if (name == "someshwari") {  
System.out.println("name is someshwari");  
}
else if(name == "jyoti") {  
System.out.println("name is jyoti");  
}
else {  
System.out.println(name);  
}  
}  
}  

